    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        Room room = new Room();
        ArrayList<ArrayList<String>> available = room.availableRoom();
        if (studId == null){
            addRoomList(available);
        } 
        else {
            Student student = new Student(studId);
            addRoomList(room.filterGender(available, student.getGender()));
        }
    }//GEN-LAST:event_formWindowOpened

    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        int index = jList1.getSelectedIndex();
        Room room = new Room();
        ArrayList<String> roomDetails;
        if (studId == null){
            roomDetails = room.availableRoom().get(index);
        }
        else {
            Student student = new Student(studId);        
            ArrayList<ArrayList<String>> finalRoom = room.filterGender(room.availableRoom(), student.getGender());
            roomDetails = finalRoom.get(index);
        }
        
        lroomNum1.setText(roomDetails.get(0));
        lgender.setText(roomDetails.get(1));
        lroomType.setText(roomDetails.get(2));
        loccupancy.setText(roomDetails.get(3));
        lavailable.setText(roomDetails.get(4));
    }//GEN-LAST:event_jList1ValueChanged

